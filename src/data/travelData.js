export function calculateDistance(origin, states) {
  let totalDistance = 0;
  states.forEach(state => {
    const key = `${origin}-${state}`;
    const reverseKey = `${state}-${origin}`;
    totalDistance += stateDistances[key] || stateDistances[reverseKey] || 1000;
  });
  return totalDistance;
}
